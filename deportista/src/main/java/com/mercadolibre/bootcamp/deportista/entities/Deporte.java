package com.mercadolibre.bootcamp.deportista.entities;


public class Deporte {

    /**
     * Atributos
     */
    private String name;
    private int level;


    /**
     * Getter & Setter
     */
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getLevel() {
        return level;
    }

    public void setLevel(int level) {
        this.level = level;
    }
}
